// Zufällige Quizoptionen generieren
function getQuizOptions(correctItem, category, count = 4) {
  const all = Object.values(VOCAB).flat();
  const wrong = all.filter(v => v.de !== correctItem.de && v.es !== correctItem.es);
  const shuffled = wrong.sort(() => Math.random() - 0.5).slice(0, count - 1);
  const options = [...shuffled, correctItem].sort(() => Math.random() - 0.5);
  return options;
}
